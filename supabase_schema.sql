CREATE TABLE IF NOT EXISTS public.subscribers (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  parent_email TEXT NOT NULL,
  age_range TEXT NOT NULL DEFAULT '3-5',
  delivery_time TIME NOT NULL DEFAULT '20:00',
  timezone TEXT NOT NULL DEFAULT 'America/New_York',
  preferred_theme TEXT NOT NULL DEFAULT 'Adventure',
  favorite_hobby TEXT NOT NULL DEFAULT 'reading',
  favorite_animal TEXT NOT NULL DEFAULT 'elephant',
  plan_type TEXT NOT NULL DEFAULT 'monthly',
  payment_status INTEGER NOT NULL DEFAULT 0, -- 0 for unpaid, 1 for paid
  package_end_date TIMESTAMP WITH TIME ZONE, -- End date of monthly package
  payment_provider_order_id TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.children (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  subscriber_id BIGINT REFERENCES public.subscribers(id) ON DELETE CASCADE,
  nickname TEXT NOT NULL,
  gender TEXT NOT NULL,
  birthday TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS public.story_jobs (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  subscriber_id BIGINT NOT NULL REFERENCES public.subscribers(id) ON DELETE CASCADE,
  story_date DATE NOT NULL,
  scheduled_generation_at TIMESTAMP WITH TIME ZONE NOT NULL,
  scheduled_delivery_at TIMESTAMP WITH TIME ZONE NOT NULL,
  status TEXT NOT NULL DEFAULT 'pending_generation',
  prompt_payload JSONB NOT NULL DEFAULT '{}'::jsonb,
  story_text TEXT,
  error_message TEXT,
  generation_attempts INTEGER NOT NULL DEFAULT 0,
  last_generation_failed_at TIMESTAMP WITH TIME ZONE,
  generated_at TIMESTAMP WITH TIME ZONE,
  sent_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE (subscriber_id, story_date)
);

CREATE TABLE IF NOT EXISTS public.story_audio (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  story_job_id UUID NOT NULL REFERENCES public.story_jobs(id) ON DELETE CASCADE,
  storage_bucket TEXT NOT NULL DEFAULT 'story-audio',
  storage_path TEXT NOT NULL,
  mime_type TEXT NOT NULL DEFAULT 'audio/mpeg',
  file_size_bytes BIGINT,
  duration_seconds INTEGER,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE (story_job_id)
);

CREATE TABLE IF NOT EXISTS public.delivery_attempts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  story_job_id UUID NOT NULL REFERENCES public.story_jobs(id) ON DELETE CASCADE,
  parent_email TEXT NOT NULL,
  status TEXT NOT NULL DEFAULT 'pending',
  provider_message_id TEXT,
  error_message TEXT,
  attempted_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

