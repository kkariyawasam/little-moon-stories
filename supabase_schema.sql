CREATE OR REPLACE FUNCTION public.claim_ready_story_generations(batch_size INTEGER DEFAULT 5)
RETURNS TABLE (
  id UUID,
  subscriber_id BIGINT,
  story_date DATE,
  scheduled_generation_at TIMESTAMP WITH TIME ZONE,
  scheduled_delivery_at TIMESTAMP WITH TIME ZONE,
  prompt_payload JSONB
)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  WITH picked AS (
    SELECT j.id
    FROM public.story_jobs j
    JOIN public.subscribers s ON s.id = j.subscriber_id
    WHERE j.status = 'pending_generation'
      AND j.scheduled_generation_at <= now()
      AND s.payment_status = 1
      AND s.package_end_date >= j.scheduled_delivery_at
    ORDER BY j.scheduled_generation_at ASC
    LIMIT batch_size
    FOR UPDATE OF j SKIP LOCKED
  ),
  updated AS (
    UPDATE public.story_jobs j
    SET
      status = 'generating',
      updated_at = now(),
      error_message = NULL,
      generation_attempts = j.generation_attempts + 1
    FROM picked p
    WHERE j.id = p.id
    RETURNING j.*
  )
  SELECT
    u.id,
    u.subscriber_id,
    u.story_date,
    u.scheduled_generation_at,
    u.scheduled_delivery_at,
    u.prompt_payload
  FROM updated u;
$$;

CREATE OR REPLACE FUNCTION public.mark_story_generation_failed(
  p_story_job_id UUID,
  p_error_message TEXT,
  p_retry BOOLEAN DEFAULT true
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  UPDATE public.story_jobs
  SET
    status = CASE
      WHEN p_retry = true AND generation_attempts < 3 THEN 'pending_generation'
      ELSE 'generation_failed'
    END,
    scheduled_generation_at = CASE
      WHEN p_retry = true AND generation_attempts < 3 THEN now() + interval '10 minutes'
      ELSE scheduled_generation_at
    END,
    updated_at = now(),
    last_generation_failed_at = now(),
    error_message = p_error_message
  WHERE id = p_story_job_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.claim_ready_story_deliveries(batch_size INTEGER DEFAULT 100)
RETURNS TABLE (
  story_job_id UUID,
  subscriber_id BIGINT,
  parent_email TEXT,
  story_date DATE,
  scheduled_delivery_at TIMESTAMP WITH TIME ZONE,
  storage_bucket TEXT,
  storage_path TEXT,
  mime_type TEXT,
  story_text TEXT
)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  WITH picked AS (
    SELECT j.id
    FROM public.story_jobs j
    JOIN public.subscribers s ON s.id = j.subscriber_id
    JOIN public.story_audio a ON a.story_job_id = j.id
    WHERE j.status = 'ready_to_send'
      AND j.scheduled_delivery_at <= now() + interval '5 minutes'
      AND s.payment_status = 1
      AND s.package_end_date >= now()
    ORDER BY j.scheduled_delivery_at ASC
    LIMIT batch_size
    FOR UPDATE OF j SKIP LOCKED
  ),
  updated AS (
    UPDATE public.story_jobs j
    SET
      status = 'sending',
      updated_at = now()
    FROM picked p
    WHERE j.id = p.id
    RETURNING j.*
  )
  SELECT
    u.id,
    u.subscriber_id,
    s.parent_email,
    u.story_date,
    u.scheduled_delivery_at,
    a.storage_bucket,
    a.storage_path,
    a.mime_type,
    u.story_text
  FROM updated u
  JOIN public.subscribers s ON s.id = u.subscriber_id
  JOIN public.story_audio a ON a.story_job_id = u.id;
$$;

CREATE OR REPLACE FUNCTION public.mark_story_delivery_sent(
  p_story_job_id UUID,
  p_provider_message_id TEXT DEFAULT NULL
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  target_email TEXT;
BEGIN
  SELECT s.parent_email
    INTO target_email
  FROM public.story_jobs j
  JOIN public.subscribers s ON s.id = j.subscriber_id
  WHERE j.id = p_story_job_id;

  UPDATE public.story_jobs
  SET
    status = 'sent',
    sent_at = now(),
    updated_at = now(),
    error_message = NULL
  WHERE id = p_story_job_id;

  INSERT INTO public.delivery_attempts (
    story_job_id,
    parent_email,
    status,
    provider_message_id
  )
  VALUES (
    p_story_job_id,
    target_email,
    'sent',
    p_provider_message_id
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.mark_story_delivery_failed(
  p_story_job_id UUID,
  p_error_message TEXT
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  target_email TEXT;
BEGIN
  SELECT s.parent_email
    INTO target_email
  FROM public.story_jobs j
  JOIN public.subscribers s ON s.id = j.subscriber_id
  WHERE j.id = p_story_job_id;

  UPDATE public.story_jobs
  SET
    status = 'delivery_failed',
    updated_at = now(),
    error_message = p_error_message
  WHERE id = p_story_job_id;

  INSERT INTO public.delivery_attempts (
    story_job_id,
    parent_email,
    status,
    error_message
  )
  VALUES (
    p_story_job_id,
    target_email,
    'failed',
    p_error_message
  );
END;
$$;

