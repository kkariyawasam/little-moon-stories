CREATE OR REPLACE FUNCTION public.enqueue_tomorrow_story_jobs()
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  inserted_count INTEGER;
BEGIN
  INSERT INTO public.story_jobs (
    subscriber_id,
    story_date,
    scheduled_generation_at,
    scheduled_delivery_at,
    prompt_payload
  )
  SELECT
    s.id,
    job.story_date,
    job.scheduled_delivery_at - interval '1 day',
    job.scheduled_delivery_at,
    jsonb_build_object(
      'parent_email', s.parent_email,
      'age_range', s.age_range,
      'preferred_theme', s.preferred_theme,
      'favorite_hobby', s.favorite_hobby,
      'favorite_animal', s.favorite_animal,
      'timezone', s.timezone,
      'delivery_time', s.delivery_time,
      'children', coalesce(
        jsonb_agg(
          jsonb_build_object(
            'nickname', c.nickname,
            'gender', c.gender,
            'birthday', c.birthday
          )
          ORDER BY c.id
        ) filter (where c.id is not null),
        '[]'::jsonb
      )
    )
  FROM public.subscribers s
  CROSS JOIN LATERAL (
    SELECT
      ((now() AT TIME ZONE s.timezone)::date + interval '1 day')::date AS story_date,
      (
        (((now() AT TIME ZONE s.timezone)::date + interval '1 day')::date + s.delivery_time)
        AT TIME ZONE s.timezone
      ) AS scheduled_delivery_at
  ) job
  LEFT JOIN public.children c ON c.subscriber_id = s.id
  WHERE s.payment_status = 1
    AND s.package_end_date >= job.scheduled_delivery_at
  GROUP BY s.id, job.story_date, job.scheduled_delivery_at
  ON CONFLICT (subscriber_id, story_date) DO NOTHING;

  GET DIAGNOSTICS inserted_count = ROW_COUNT;
  RETURN inserted_count;
END;
$$;

CREATE OR REPLACE FUNCTION public.enqueue_signup_story_job(
  p_subscriber_id BIGINT,
  p_minimum_lead_time INTERVAL DEFAULT interval '5 hours'
)
RETURNS UUID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  inserted_story_job_id UUID;
BEGIN
  INSERT INTO public.story_jobs (
    subscriber_id,
    story_date,
    scheduled_generation_at,
    scheduled_delivery_at,
    prompt_payload
  )
  SELECT
    s.id,
    job.story_date,
    now(),
    job.scheduled_delivery_at,
    jsonb_build_object(
      'parent_email', s.parent_email,
      'age_range', s.age_range,
      'preferred_theme', s.preferred_theme,
      'favorite_hobby', s.favorite_hobby,
      'favorite_animal', s.favorite_animal,
      'timezone', s.timezone,
      'delivery_time', s.delivery_time,
      'children', coalesce(
        jsonb_agg(
          jsonb_build_object(
            'nickname', c.nickname,
            'gender', c.gender,
            'birthday', c.birthday
          )
          ORDER BY c.id
        ) filter (where c.id is not null),
        '[]'::jsonb
      )
    )
  FROM public.subscribers s
  CROSS JOIN LATERAL (
    SELECT
      (now() AT TIME ZONE s.timezone)::date AS story_date,
      (
        ((now() AT TIME ZONE s.timezone)::date + s.delivery_time)
        AT TIME ZONE s.timezone
      ) AS scheduled_delivery_at
  ) job
  LEFT JOIN public.children c ON c.subscriber_id = s.id
  WHERE s.id = p_subscriber_id
    AND s.payment_status = 1
    AND job.scheduled_delivery_at >= now() + p_minimum_lead_time
    AND s.package_end_date >= job.scheduled_delivery_at
  GROUP BY s.id, job.story_date, job.scheduled_delivery_at
  ON CONFLICT (subscriber_id, story_date) DO NOTHING
  RETURNING id INTO inserted_story_job_id;

  RETURN inserted_story_job_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.enqueue_signup_story_job_with_message(
  p_subscriber_id BIGINT,
  p_minimum_lead_time INTERVAL DEFAULT interval '5 hours'
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  subscriber_record public.subscribers%ROWTYPE;
  child_count INTEGER;
  local_story_date DATE;
  scheduled_delivery_at TIMESTAMP WITH TIME ZONE;
  existing_story_job_id UUID;
  inserted_story_job_id UUID;
BEGIN
  SELECT *
    INTO subscriber_record
  FROM public.subscribers
  WHERE id = p_subscriber_id;

  IF NOT FOUND THEN
    RETURN jsonb_build_object(
      'ok', false,
      'code', 'subscriber_not_found',
      'message', 'No subscriber was found for this id.'
    );
  END IF;

  IF subscriber_record.payment_status <> 1 THEN
    RETURN jsonb_build_object(
      'ok', false,
      'code', 'not_paid',
      'message', 'This parent is registered but not paid yet, so no same-day story job can be created.'
    );
  END IF;

  SELECT count(*)
    INTO child_count
  FROM public.children
  WHERE subscriber_id = p_subscriber_id;

  IF child_count = 0 THEN
    RETURN jsonb_build_object(
      'ok', false,
      'code', 'missing_children',
      'message', 'This subscriber has no child profile, so the story prompt would be incomplete.'
    );
  END IF;

  local_story_date := (now() AT TIME ZONE subscriber_record.timezone)::date;
  scheduled_delivery_at := (
    (local_story_date + subscriber_record.delivery_time)
    AT TIME ZONE subscriber_record.timezone
  );

  SELECT id
    INTO existing_story_job_id
  FROM public.story_jobs
  WHERE subscriber_id = p_subscriber_id
    AND story_date = local_story_date
  LIMIT 1;

  IF existing_story_job_id IS NOT NULL THEN
    RETURN jsonb_build_object(
      'ok', true,
      'code', 'already_exists',
      'story_job_id', existing_story_job_id,
      'message', 'A same-day story job already exists for this parent.'
    );
  END IF;

  IF scheduled_delivery_at < now() + p_minimum_lead_time THEN
    RETURN jsonb_build_object(
      'ok', false,
      'code', 'not_enough_lead_time',
      'message', format(
        'Same-day delivery was not created because the selected delivery time is less than %s away.',
        p_minimum_lead_time
      ),
      'scheduled_delivery_at', scheduled_delivery_at
    );
  END IF;

  IF subscriber_record.package_end_date IS NULL
     OR subscriber_record.package_end_date < scheduled_delivery_at THEN
    RETURN jsonb_build_object(
      'ok', false,
      'code', 'package_not_active_for_delivery',
      'message', 'The monthly package does not cover the selected same-day delivery time.',
      'scheduled_delivery_at', scheduled_delivery_at,
      'package_end_date', subscriber_record.package_end_date
    );
  END IF;

  inserted_story_job_id := public.enqueue_signup_story_job(
    p_subscriber_id,
    p_minimum_lead_time
  );

  IF inserted_story_job_id IS NULL THEN
    RETURN jsonb_build_object(
      'ok', false,
      'code', 'not_created',
      'message', 'No story job was created. Please check payment status, package date, delivery time, and duplicate jobs.'
    );
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'code', 'created',
    'story_job_id', inserted_story_job_id,
    'message', 'Same-day story job created successfully.',
    'scheduled_delivery_at', scheduled_delivery_at
  );
END;
$$;

